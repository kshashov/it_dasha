package org.github.dkovaleva.calendar;

public class TaskTime {
    private int year;
    private int day;

    public TaskTime() {
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public int getDay() {
        return day;
    }

    public void setDay(int day) {
        this.day = day;
    }
}
